tabuada = 1
while tabuada <= 10:
    numero = 1
    print(f' \n Tabuada do {tabuada} \n')
    while numero <= 10:
        print(f' {tabuada} x {numero} = { tabuada * numero}')
        # print(' ' + str(tabuada) + ' x '+ str(numero) + ' = ' + str(tabuada * numero)  )
        numero += 1
    tabuada += 1